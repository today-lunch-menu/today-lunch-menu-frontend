function createFoodRecord(obj: {
  readonly [key: string]: { keyword: string; name?: string; image?: string };
}) {
  const foods = Object.entries(obj);

  for (let i = 0; i < foods.length; i++) {
    const [key] = foods[i];

    obj[key].name = key;
    obj[key].image = `/images/${obj[key].name}.png`;
  }
}

